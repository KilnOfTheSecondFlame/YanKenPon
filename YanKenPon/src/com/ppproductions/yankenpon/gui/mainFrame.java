    private void combatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_combatActionPerformed
        // TODO add your handling code here:
        pMove = evt.getActionCommand();
        //oMove = d.getOpponentMove();
        
        // Display moves and result:
        combatScreen.append(pName + ": " + pMove + "\n");
        d.fight(pMove, combatScreen, o);
        oMoveField.setText(d.getOpponentMove());
        update();
        
        
    }//GEN-LAST:event_combatActionPerformed

    public void update() {
        scoreLabel.setText(d.getScore());
        if (d.getWinState() != 0) {
            rockButton.setEnabled(false);
            PaperButton.setEnabled(false);
            scissorsButton.setEnabled(false);
            if (d.getWinState() > 0) combatScreen.append("YOU WIN!!!!");
            else combatScreen.append("YOU LOSE!!!!");
        }
    }
    
